    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String proid = request.getParameter("projectid");
        request.setAttribute("proid", proid);
        request.getRequestDispatcher("view/AddSection.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String proid = request.getParameter("projectid");
        String name = request.getParameter("name");
        String startdate = LocalDate.now().toString();
        int id;
        SectionDAO s = new SectionDAO();
        try {
            if (s.getAll().isEmpty()) {
                id = 1;
            } else {
                id = s.getAll().get(s.getAll().size() - 1).getId() + 1;
            }            
            s.Insert(id, name, startdate, Integer.parseInt(proid));
            response.sendRedirect("projectcheck?id=" + proid);

        } catch (Exception ex) {
            Logger.getLogger(AddTask.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

