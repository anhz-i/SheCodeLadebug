package model;

/**
 *
 * @author Hp
 */
public class Task {

    private int id;
    private String name, startDate, dueDate, description;
    private int priority;
    private String status, sectionID, projectID;
    private int userID;
    private String image;

    public Task() {
    }

    public Task(int id, String name, String startDate, String dueDate, String description, int priority, String status, String sectionID, String projectID, int userID, String image) {
        this.id = id;
        this.name = name;
        this.startDate = startDate;
        this.dueDate = dueDate;
        this.description = description;
        this.priority = priority;
        this.status = status;
        this.sectionID = sectionID;
        this.projectID = projectID;
        this.userID = userID;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getDueDate() {
        return dueDate;
    }

    public void setDueDate(String dueDate) {
        this.dueDate = dueDate;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSectionID() {
        return sectionID;
    }

    public void setSectionID(String sectionID) {
        this.sectionID = sectionID;
    }

    public String getProjectID() {
        return projectID;
    }

    public void setProjectID(String projectID) {
        this.projectID = projectID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    
}
